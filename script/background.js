function main() {
	for (var element of document.getElementsByTagName("article")) {
		element.style.transform = "translateX(0)";
		element.style.opacity = 100;
	}
	for (var element of document.getElementsByTagName("aside")) {
		element.style.transform = "translateX(0)";
		element.style.opacity = 100;
	}
}

	
